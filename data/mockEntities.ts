// 헬퍼 함수: ID로 엔티티 찾기
export function getEntityById(id: number): EntityMaster | undefined {
  return MOCK_ENTITIES.find((entity) => entity.id === id);
}

// 헬퍼 함수: Element로 엔티티 필터링
export function getEntitiesByElement(element: string): EntityMaster[] {
  return MOCK_ENTITIES.filter((entity) => entity.element === element);
}

// 헬퍼 함수: Rarity로 엔티티 필터링
export function getEntitiesByRarity(rarity: string): EntityMaster[] {
  return MOCK_ENTITIES.filter((entity) => entity.rarity === rarity);
}

// 헬퍼 함수: ID 범위로 엔티티 필터링 (맵별 출현용)
export function getEntitiesByIdRange(min: number, max: number): EntityMaster[] {
  return MOCK_ENTITIES.filter((entity) => entity.id >= min && entity.id <= max);
}
